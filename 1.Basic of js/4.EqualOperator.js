// There are (==) and (===) equals in js 

// == used for equal value//
var x=10;
var y=10;
if(x==y)
{ 
    console.log(true); //return true//
}
else{
    console.log(false);
}

// === used for equal value and equal type//
if(x===y)
{
    console.log(true);  //returns true
}
else{
    console.log(false);
}


var num1=20;;
var string1='20';
if(num1===string1)
{
    console.log(true);
}
else{
    console.log(false); //return false//
}
